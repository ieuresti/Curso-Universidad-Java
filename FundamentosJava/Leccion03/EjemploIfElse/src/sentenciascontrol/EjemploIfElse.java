package sentenciascontrol;

public class EjemploIfElse {

    public static void main(String[] args) {
        //Ejemplo if else
        int x = 30;
        if (x < 20) {
            System.out.println("X es menor que 20\n");
        } else {
            System.out.println("X es mayor a 20\n");
        }
    }
    
}
