package sentenciascontrol;

public class EjemploIfElseIfElse {

    public static void main(String[] args) {
        //Ejemplo if else/ if else (anidado)
        int x = 10;
        if (x == 50) {
            System.out.println("X es igual a 10\n");
        } else if (x == 20) {
            System.out.println("X es igual a 20\n");
        } else if (x == 30) {
            System.out.println("X es igual a 30\n");
        } else {
            System.out.println("X no es igual ni a 10, ni a 20, ni a 30\n");
        }
    }
    
}
